// Идемпотентный seed: вставляет статьи только если у тенанта нет ни одной.
// Безопасно вызывать многократно — повторные вызовы no-op.
export async function seedKnowledgeBaseIfEmpty(
  tenantId: string,
  supabase: ReturnType<typeof createAdminClient>
): Promise<{ seeded: number }> {
  const { count } = await supabase
    .from('tenant_knowledge_articles')
    .select('id', { count: 'exact', head: true })
    .eq('tenant_id', tenantId)

  if ((count ?? 0) > 0) return { seeded: 0 }

  const rows = KB_SEED_ARTICLES.map(a => ({
    tenant_id: tenantId,
    title: a.title,
    content: a.content,
    is_active: true,
  }))

  const { error } = await supabase.from('tenant_knowledge_articles').insert(rows)
  if (error) {
    console.error('[kb-seed] insert failed:', error.message)
    return { seeded: 0 }
  }
  return { seeded: rows.length }
}
